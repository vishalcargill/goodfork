  INSERT INTO "Ingredient" ("id", "slug", "name", "defaultUnit", "allergens", "tags")
  SELECT gen_random_uuid()::text, slug, name, unit, '{}'::text[], '{}'::text[]
  FROM ingredient_payload
  ON CONFLICT ("slug") DO UPDATE
    SET "name" = EXCLUDED."name",
        "defaultUnit" = EXCLUDED."defaultUnit"
